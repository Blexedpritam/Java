public class Demo2 {
    public static void main(String[] args) {
        //Relational Operators . -->  == , != , < , > , <= , >=  
       
        int a = 5 ;
        int b = 10;
        
        boolean c = ( a == b); // Flase
        System.out.println(c);

        boolean d = (a != b);  // True 
        System.out.println(d);

        boolean e  = (a <= b); // True 
        boolean f = (a > b); // Flase

        System.out.println( e + " ," + f);
    }
}
